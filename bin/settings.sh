#!/bin/bash
# ClawsJoy 统一 shell 配置

if [ -z "${CLAWSJOY_ROOT:-}" ]; then
    if [ -d "/mnt/d/clawsjoy" ]; then
        CLAWSJOY_ROOT="/mnt/d/clawsjoy"
    elif [ -d "/root/clawsjoy" ]; then
        CLAWSJOY_ROOT="/root/clawsjoy"
    else
        CLAWSJOY_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
    fi
fi

CLAWSJOY_TENANTS_DIR="${CLAWSJOY_TENANTS_DIR:-$CLAWSJOY_ROOT/tenants}"
CLAWSJOY_DATA_DIR="${CLAWSJOY_DATA_DIR:-$CLAWSJOY_ROOT/data}"
CLAWSJOY_LOGS_DIR="${CLAWSJOY_LOGS_DIR:-$CLAWSJOY_ROOT/logs}"
CLAWSJOY_TASK_QUEUE_DIR="${CLAWSJOY_TASK_QUEUE_DIR:-$CLAWSJOY_TENANTS_DIR/queue}"
CLAWSJOY_LOG_APP_DIR="${CLAWSJOY_LOG_APP_DIR:-$CLAWSJOY_LOGS_DIR/app}"
CLAWSJOY_LOG_RUNNER_DIR="${CLAWSJOY_LOG_RUNNER_DIR:-$CLAWSJOY_LOGS_DIR/runner}"
CLAWSJOY_LOG_AUDIT_DIR="${CLAWSJOY_LOG_AUDIT_DIR:-$CLAWSJOY_LOGS_DIR/audit}"
CLAWSJOY_LOG_SYSTEM_DIR="${CLAWSJOY_LOG_SYSTEM_DIR:-$CLAWSJOY_LOGS_DIR/system}"

# 日志轮转策略（供安装 logrotate 配置时使用）
CLAWSJOY_LOG_ROTATE_COUNT="${CLAWSJOY_LOG_ROTATE_COUNT:-14}"
CLAWSJOY_LOG_ROTATE_SIZE="${CLAWSJOY_LOG_ROTATE_SIZE:-20M}"

CLAWSJOY_PORT_AUTH="${CLAWSJOY_PORT_AUTH:-8092}"
CLAWSJOY_PORT_TENANT="${CLAWSJOY_PORT_TENANT:-8088}"
CLAWSJOY_PORT_BILLING="${CLAWSJOY_PORT_BILLING:-8090}"
CLAWSJOY_PORT_TASK="${CLAWSJOY_PORT_TASK:-8084}"
CLAWSJOY_PORT_JOYMATE="${CLAWSJOY_PORT_JOYMATE:-8080}"
CLAWSJOY_PORT_COFFEE="${CLAWSJOY_PORT_COFFEE:-8085}"
CLAWSJOY_PORT_PROMO="${CLAWSJOY_PORT_PROMO:-8086}"
